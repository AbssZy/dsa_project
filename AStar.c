int minDistance(int dist,int h[])
{
 int min = INT_MAX, min_index;
 for (int v = 0; v < V; v++)
 if (dist+h[v] <= min)
 min = dist, min_index = v;
 return min_index;
}
void printSolution(int dist)
{
 printf("Vertex Distance from Source is :%d ",dist);
}
